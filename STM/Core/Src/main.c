void DHT11_Start(void) {
    DHT11_SetPinOutput();    // MCU drives the line

    HAL_GPIO_WritePin(DHT11_PORT, DHT11_PIN, GPIO_PIN_RESET);
    HAL_Delay(18);           // pull low for 18 ms

    HAL_GPIO_WritePin(DHT11_PORT, DHT11_PIN, GPIO_PIN_SET);
    delay_us(30);            // pull high for 20â€“40 us

    DHT11_SetPinInput();     // release, wait for response
}

uint8_t DHT11_CheckResponse(void) {
    uint8_t response = 0;
    delay_us(40);

    if (!HAL_GPIO_ReadPin(DHT11_PORT, DHT11_PIN)) {
        delay_us(80);
        if (HAL_GPIO_ReadPin(DHT11_PORT, DHT11_PIN)) response = 1;
        delay_us(80);
    }

    return response;
}

uint8_t DHT11_ReadBit(void) {
    while (!HAL_GPIO_ReadPin(DHT11_PORT, DHT11_PIN));  // wait for pin high
    delay_us(40);
    if (HAL_GPIO_ReadPin(DHT11_PORT, DHT11_PIN))
        return 1;
    else
        return 0;
}

uint8_t DHT11_ReadByte(void) {
    uint8_t i, result = 0;
    for (i = 0; i < 8; i++) {
        result <<= 1;
        result |= DHT11_ReadBit();
        while (HAL_GPIO_ReadPin(DHT11_PORT, DHT11_PIN)); // wait for low
    }
    return result;
}



uint8_t DHT11_Read(void) {
    uint8_t hum_i, hum_d, tem_i, tem_d, check; // Local variables

    DHT11_Start();
    if (DHT11_CheckResponse()) {
        hum_i = DHT11_ReadByte();
        hum_d = DHT11_ReadByte();
        tem_i = DHT11_ReadByte();
        tem_d = DHT11_ReadByte();
        check = DHT11_ReadByte();

        // Only update globals if checksum matches
        if ((hum_i + hum_d + tem_i + tem_d) == check) {
            humidity_int = hum_i;
            humidity_dec = hum_d;
            temp_int     = tem_i; // temp_int is only updated on SUCCESS now
            temp_dec     = tem_d;
            checksum     = check;
            return 1;  // OK
        }
    }
    return 0;  // FAIL
}

